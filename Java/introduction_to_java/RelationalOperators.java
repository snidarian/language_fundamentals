public class RelationalOperators {
    
    public static void main(String[] args) {
        

        boolean morethan = 1 > 2;
        boolean lessthan = 1 < 2;
        
        boolean morethanorequalto = 1>=2;
        boolean lessthanorequalto = 1<=2;

        boolean equalto = 1 == 2;
        boolean notequalto = 1 !=2;

        
        
        // The result is false, true, false, true, false, true
        System.out.println(morethan);
        System.out.println(lessthan);
        System.out.println(morethanorequalto);
        System.out.println(lessthanorequalto);
        System.out.println(equalto);
        System.out.println(notequalto);
    }
}
