public class ArithmeticOperators {
    public static void main(String[] args) {
        
        /*
        
        addition +
        subtraction -
        multiplication*
        division /

        Modulus/remainder %
        Increment ++
        Decrement --

        */

        int a = 15;

        // example of the use of all of these arithmetic operators
        System.out.println(a + 12);
        System.out.println(a - 12);
        System.out.println(a * 12);
        System.out.println(a / 12);
        System.out.println(a % 12);
        

        // incrementation/decrementation
        a--;
        a++;


    }
}
